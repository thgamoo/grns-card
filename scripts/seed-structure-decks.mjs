function buildCard(classId, row) {
  const [id, serial, name, cost, type, race, power, sigil, effect, role] = row;
  const classInfo = classes[classId];
  return {
    id,
    name,
    cost,
    faction: classInfo.faction,
    classId,
    className: classInfo.className,
    theme: classInfo.theme,
    type,
    race,
    effect,
    role,
    power,
    actionCost: 1,
    serial,
    sigil,
    illustration: "",
  };
}

const cards = Object.entries(cardData).flatMap(([classId, rows]) => rows.map((row) => buildCard(classId, row)));

function buildDeck(classId) {
  const classInfo = classes[classId];
  const entries = [];
  for (const [type, counts] of typePlans) {
    const typeCards = cards.filter((card) => card.classId === classId && card.type === type);
    typeCards.forEach((card, index) => {
      entries.push({ cardId: card.id, serial: card.serial, count: counts[index] });
    });
  }
  return {
    id: `${classId}-structure-v1`,
    name: `${classInfo.faction} ${classInfo.className} 스트럭쳐 덱`,
    classId,
    faction: classInfo.faction,
    className: classInfo.className,
    totalCards: entries.reduce((sum, entry) => sum + entry.count, 0),
    trapCards: entries
      .filter((entry) => cards.find((card) => card.id === entry.cardId)?.type === "트랩유닛")
      .reduce((sum, entry) => sum + entry.count, 0),
    entries,
  };
}

